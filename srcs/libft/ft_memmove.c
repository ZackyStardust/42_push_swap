#include <libft.h>

void	*ft_memmove(void *dst, const void *src, int len)
{
	int	i;

	i = len;
	if (!src && !dst)
		return (0);
	if (dst > src)
	{
		while (--i >= 0 && i < len)
			((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
	}
	else
	{
		i = 0;
		while (i < len)
		{
			((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
			i++;
		}
	}
	return (dst);
}
